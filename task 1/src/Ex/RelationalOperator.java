package Ex;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a=7, b=11;
		
		System.out.println("a = "+ a + "and b = " + b);
		
		System.out.println(a == b); //false
		
		System.out.println(a != b); // true
		
		System.out.println(a > b); //false
		
		System.out.println(a < b);  // true
		
		System.out.println(a >= b); //false 
		
		System.out.println(a <= b); //true
		
		
		
	}

}
